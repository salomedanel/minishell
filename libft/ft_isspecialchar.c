#include "libft.h"

int	ft_isspecialchar(int c)
{
	if (c == 39 || c == '"' || c == '\n' || c == '\v' || c == '\f'
		|| c == '\r' || c == '@' || c == '-' || c == '.' || c == '+'
		|| c == '*' || c == '{' || c == '#' || c == '}' || c == '%'
		|| c == '?' || c == '/' || c == '^')
		return (1);
	return (0);
}
